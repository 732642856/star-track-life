/**
 * 分析四化 - 心理动机
 */
function analyzeSiHuaPsychology(fourTransformations) {
    const analysis = {
        欲望领域: [],
        执念领域: [],
        防御机制: [],
        阿喀琉斯之踵: ''
    };

    for (const [trans, data] of Object.entries(fourTransformations)) {
        if (SIHUA_PSYCHOLOGY_MAPPING[trans]) {
            const psychMapping = SIHUA_PSYCHOLOGY_MAPPING[trans];

            if (trans === '化禄') {
                analysis.欲望领域.push({
                    宫位: data.palace,
                    描述: psychMapping.性格表现.join('；')
                });
            } else if (trans === '化权') {
                analysis.防御机制.push({
                    宫位: data.palace,
                    描述: psychMapping.性格表现.join('；')
                });
            } else if (trans === '化忌') {
                analysis.执念领域.push({
                    宫位: data.palace,
                    描述: psychMapping.性格表现.join('；')
                });
                analysis.阿喀琉斯之踵 = `${data.palace}位置的${trans}，表现为${psychMapping.性格表现[0]}`;
            }
        }
    }

    return analysis;
}

