// ==================== 随机工具函数 ====================

/** 从数组随机选一个 */
function _pick(arr) {
    if (!arr || arr.length === 0) return '';
    return arr[Math.floor(Math.random() * arr.length)];
}

/** 从数组随机选 n 个不重复元素 */
function _pickN(arr, n) {
    if (!arr || arr.length === 0) return [];
    const shuffled = arr.slice().sort(() => Math.random() - 0.5);
    return shuffled.slice(0, Math.min(n, shuffled.length));
}

/** 根据字符串生成确定性伪随机数（0~1），相同输入总是同一输出 */
function _hash(str) {
    let h = 0;
    for (let i = 0; i < str.length; i++) {
        h = (Math.imul(31, h) + str.charCodeAt(i)) | 0;
    }
    return Math.abs(h) / 2147483647;
}

/** 从数组按 key（人物名+字段）确定性选一个（同一人物同一字段始终相同） */
function _pickDet(arr, key) {
    if (!arr || arr.length === 0) return '';
    const idx = Math.floor(_hash(key) * arr.length);
    return arr[idx];
}

/** 确定性选 n 个 */
function _pickNDet(arr, n, key) {
    if (!arr || arr.length === 0) return [];
    const sorted = arr.slice().sort((a, b) => {
        const ha = _hash(key + a);
        const hb = _hash(key + b);
        return ha - hb;
    });
    return sorted.slice(0, Math.min(n, sorted.length));
}

