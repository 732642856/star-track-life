/**
 * 用词汇选取结果组装人物小传
 */
function assembleBio(lex, attrs, chartData) {
    const pr = (attrs.gender === '男' || attrs.gender === 'male') ? '他' : '她';
    const name = attrs.name || (pr === '他' ? '男主' : '女主');
    const role = attrs.role || selectedSubPattern || '命主';
    const eraMap = {
        ancient:'古代江湖', modern:'民国乱世', contemporary:'当代都市',
        '当代都市':'当代都市', '民国乱世':'民国乱世', '古代江湖':'古代江湖',
    };
    const eraName = eraMap[attrs.era] || attrs.era || '当代都市';

    const { mainStar, brightness, bMod, aura, desire, fear, behavior, shadow,
            dizhiTone, dizhiPace, dizhiWorldview, sihuaLines, patternType, sihuaType } = lex;

    // 四化命运线（取最关键的2条）
    const fateLine = sihuaLines.slice(0, 2).map(s => `${s.sihua}入${s.palace}——${s.line}`).join('；');

    // 8属性补充层
    const attrLines = buildAttrLines(attrs, pr);

    // 命盘唯一ID标注（编剧备用）
    const uid = chartData.chartUid || chartData.chartId || '—';

    const parts = [
        `【人物小传：${name}】`,
        `盘型：${patternType} · ${sihuaType} · ${mainStar || '空宫'}${brightness !== '平' ? '（'+brightness+'）' : ''} · 命宫${chartData.mingDizhi || ''}宫`,
        ``,
        `【外在气场】`,
        `${eraName}里，${bMod.prefix}${aura}。${dizhiTone}，${dizhiPace}。`,
        ``,
        `【灵魂驱动】`,
        `${pr}${bMod.intensity}渴望${desire}。`,
        `${dizhiWorldview}——这是${pr}的处世底色。`,
        ``,
        `【命运底牌】`,
        fateLine ? fateLine : `${mainStar || '命主'}为${patternType}底色，四化无重大落命。`,
        ``,
        `【行为模式】`,
        `${pr}的日常：${behavior}。`,
        `这不是性格，是应对世界的方式——${pr}花了很多年才让它变得自动。`,
        ``,
        `【人性阴影】`,
        `${pr}最深的裂缝：${shadow}。`,
        `最不能接受的事：${fear}。`,
        ``,
    ];

    // 8属性维度（步骤4选项）
    if (attrLines.length > 0) {
        parts.push('【人物维度】');
        parts.push(...attrLines);
        parts.push('');
    }

    // 角色定位
    parts.push('【角色定位】');
    parts.push(`${pr}在这个故事里是「${role}」。`);
    parts.push(buildClosingLine(mainStar, patternType, sihuaType, pr, name));
    parts.push('');
    parts.push(`※ 盘坐标 ${uid} ※`);

    return parts.join('\n');
}

