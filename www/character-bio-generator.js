/**
 * 生成完整人物小传（主接口，供app-v2.js调用）
 * @param {Object} config - { attrs, chartData, personality }
 */
function generateCharacterBio(config) {
    const { attrs = {}, chartData = null } = config;
    if (!chartData) {
        return `【人物小传】\n命盘数据缺失，请完成星盘匹配后再生成。`;
    }
    const lex = pickLexicon(chartData);
    return assembleBio(lex, attrs, chartData);
}

/**
 * 生成人格profile（供步骤4预览用）
 */
function generatePersonalityProfile(attrs, chartData) {
    if (!chartData) return {};
    const lex = pickLexicon(chartData);
    return {
        aura: lex.aura,
        desire: lex.desire,
        fear: lex.fear,
        behavior: lex.behavior,
        shadow: lex.shadow,
        sihuaLines: lex.sihuaLines,
    };
}

