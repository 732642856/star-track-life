// ==================== 坐标→词汇选取器 ====================

/**
 * 由命盘唯一坐标选取词汇
 * @param {Object} chartData - generate144Chart返回的完整命盘
 * @returns {Object} 各维度选取结果
 */
function pickLexicon(chartData) {
    const {
        mingStars = [], mingStarBrightness = {},
        mingDizhi = '子',
        fourTrans = {}, sihuaPalaceMap = {},
        patternType, sihuaType, yearGan,
        keIdx = 0, gender,
    } = chartData;

    // ── [A] 主星选词 ──────────────────────────────────────────────────
    // 命宫主星（优先取第一颗有完整词库的）
    const mainStar = mingStars.find(s => STAR_LEXICON[s]) || mingStars[0];
    const starLex = STAR_LEXICON[mainStar] || STAR_LEXICON['天机'];

    // 亮度档
    const brightness = mainStar ? (mingStarBrightness[mainStar] || '平') : '平';
    const bMod = BRIGHTNESS_MODIFIER[brightness] || BRIGHTNESS_MODIFIER['平'];

    // keIdx(0-7) 用于在同一档内选不同句子（每档3句，keIdx%3选句）
    const kPick = (arr) => arr[keIdx % arr.length];
    const gPick = (arr) => arr[gender === 'female' ? (arr.length > 1 ? 1 : 0) : 0];

    // ── [B] 宫位底色 ──────────────────────────────────────────────────
    const dizhiLex = MING_DIZHI_LEXICON[mingDizhi] || MING_DIZHI_LEXICON['子'];

    // ── [C] 亮度修饰 ──────────────────────────────────────────────────
    // 决定措辞的强度和前缀

    // ── [D] 四化落宫措辞 ─────────────────────────────────────────────
    const sihuaLines = [];
    for (const [sihuaName, palaceName] of Object.entries(sihuaPalaceMap)) {
        const lineMap = SIHUA_PALACE_LINES[sihuaName] || {};
        const line = lineMap[palaceName] || lineMap['命宫'] || '';
        if (line) sihuaLines.push({ sihua: sihuaName, palace: palaceName, line });
    }

    // ── 组合输出 ──────────────────────────────────────────────────────
    return {
        mainStar,
        brightness,
        bMod,
        aura:     kPick(starLex.aura),
        desire:   kPick(starLex.desire),
        fear:     kPick(starLex.fear),
        behavior: kPick(starLex.behavior),
        shadow:   kPick(starLex.shadow),
        dizhiTone:     dizhiLex.tone,
        dizhiPace:     dizhiLex.pace,
        dizhiWorldview:dizhiLex.worldview,
        sihuaLines,
        patternType,
        sihuaType,
        yearGan,
        keIdx,
    };
}

