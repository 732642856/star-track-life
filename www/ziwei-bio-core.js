    bio += `**标志性细节：** ${signature}\n\n`;
    bio += `---\n\n`;

    // ── 二、显性人格——命宫心理学 ──
    var posTraits = (patternData.traits.positive || []).slice(0, 3).join('、');
    var negTraits = (patternData.traits.negative || []).slice(0, 2).join('、');
